@app.route("/random")
def get_random_cafe():
    result = db.session.execute(db.select(Cafe))  # Renaming 'random' to 'result'
    all_cafes = result.scalars().all()
    random_cafe = random.choice(all_cafes)  # Now using 'random.choice()' correctly
    return jsonify(cafe=random_cafe.to_dict())


@app.route("/all")
def get_all_cafes():
    result = db.session.execute(db.select(Cafe).order_by(Cafe.name))
    all_cafes = result.scalars().all()
    #This uses a List Comprehension but you could also split it into 3 lines.
    return jsonify(cafes=[cafe.to_dict() for cafe in all_cafes])


@app.route("/search", methods=['GET'])
def get_cafe_at_location():
    search_location = request.args.get('loc')  # Extract 'location' from query parameters

    # Apply the 'where' filter before executing the query
    result = db.session.execute( db.select(Cafe).where(Cafe.location == search_location).order_by(Cafe.name))
    all_cafes = result.scalars().all()  # Fetch all results

    # Convert cafes to a list of dictionaries and return as JSON
    if all_cafes:
        return jsonify(cafes=[cafe.to_dict() for cafe in all_cafes])

    else:

        return jsonify(error={"Not Found": "Sorry, we don't have a cafe at that location."}), 404



@app.route("/add", methods=["POST"])
def post_new_cafe():
    new_cafe = Cafe(
        name=request.form.get("name"),
        map_url=request.form.get("map_url"),
        img_url=request.form.get("img_url"),
        location=request.form.get("location"),
        has_sockets=bool(request.form.get("sockets")),
        has_toilet=bool(request.form.get("toilet")),
        has_wifi=bool(request.form.get("wifi")),
        can_take_calls=bool(request.form.get("calls")),
        seats=request.form.get("seats"),
        coffee_price=request.form.get("coffee_price"),

    )
    db.session.add(new_cafe)
    db.session.commit()
    return jsonify(response={"success": "Successfully added the new cafe."})


@app.route("/update-price/<int:cafe_id>", methods=["PATCH"])
def patch_new_price(cafe_id):
    new_price = request.args.get("new_price")
    cafe = db.get_or_404(Cafe, cafe_id)
    if cafe:
        cafe.coffee_price = new_price
        db.session.commit()
        return jsonify(response={"success": "Successfully updated the price."})
    else:
        return jsonify(error={"Not Found": "Sorry a cafe with that id was not found in the database."})

